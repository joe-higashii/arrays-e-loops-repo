// push-pop pra quando queremos colocar ou retirar elementos no final de um array

const nomesDeEstudantes = ["jose", "joao", "maria", "joana", "andreia", "alice"];

nomesDeEstudantes.push("julio");

nomesDeEstudantes.pop();

nomesDeEstudantes[nomesDeEstudantes.lenght] = "pedro";

console.log(nomesDeEstudantes);
